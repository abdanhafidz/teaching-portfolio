#include <iostream>
using namespace std;


int main(){
    // SOAL 1
    int a = 5; // a = 5
    for(int i = 1; i <= 100; i++){
        a = a * i; 
    }
    // i = 1 -> a = 5 * 1 = 5
    // i = 2 -> a = 5 * 2 = 10
    // i = 3 -> a = 5 * 2 * 3 = 10 *3 = 30
    // i = 4 -> a = 5 * 2 * 3 * 4 =  30 *4 = 120
    // i = 5 -> a = 5 * 2 * 3 * 4 * 5 = 600
    // ...
    // i = 100 -> a = 5 * 1 * 2 * 3 * 4 * 5 * 6 * ... * 100 = 5 * 100!
    cout<<a<<endl;

    // SOAL 2
    a = 5;
    for(int i = 1; i <= 10; i++){
        a += a * i; // a = a + a * i -> a = (i + 1) * a -> ai = (i + 1) * ai-1
    }
    // i = 1 -> a += a * i -> a = 5 + 5 * 1 = 11
    // i = 2 -> a += 11 * 2 -> a = 11 + 11 * 2 = 33 -> a = (5 + 5 * 1) +  (5 + 5 * 1) * 2
    // i = 3 -> a += 33 * 3 -> a = 33 + 33 * 3 = 132 -> a = ((5 + 5 * 1) +  (5 + 5 * 1) * 2) + ((5 + 5 * 1) +  (5 + 5 * 1) * 2) * 3
    // ...
    // i = 10
    // a0 = 5
    // a1 = (1 + 1) * a0 = (1 + 1) * 5
    // a2 = (2 + 1) * a1 = (2 + 1) * (1 + 1) * 5
    // ...
    // a100 = (100 + 1) * (99 + 1) * (98 + 1) * ... * 5
    // a100 = 101 * 100 * 98 * 97 * ... * 2 * 1 * 5 = 101! * 5

    cout<<a<<endl;
}